def area(base: float, height: float) -> float:
    """
    Вычисляет площадь треугольника.

    Принимает:
        base (float): Длина основания треугольника.
        height (float): Высота треугольника, опущенная на основание.

    Возвращает:
        float: Площадь треугольника.

    Вызывает:
        ValueError: Если основание или высота отрицательны.

    Пример вызова:
    >>> area(10, 5)
    25.0
    """
    if base < 0 or height < 0:
        raise ValueError("Основание и высота треугольника не могут быть отрицательными.")
    return 0.5 * base * height

def perimeter(side1: float, side2: float, side3: float) -> float:
    """
    Вычисляет периметр треугольника.

    Принимает:
        side1 (float): Длина первой стороны треугольника.
        side2 (float): Длина второй стороны треугольника.
        side3 (float): Длина третьей стороны треугольника.

    Возвращает:
        float: Периметр треугольника.

    Вызывает:
        ValueError: Если какая-либо сторона отрицательна или если стороны не образуют валидный треугольник.

    Пример вызова:
    >>> perimeter(3, 4, 5)
    12.0
    """
    if side1 < 0 or side2 < 0 or side3 < 0:
        raise ValueError("Длины сторон треугольника не могут быть отрицательными.")

    # Проверка на неравенство треугольника: сумма длин двух любых сторон должна быть строго больше третьей стороны
    if not (side1 + side2 > side3 and
            side1 + side3 > side2 and
            side2 + side3 > side1):
        raise ValueError("Предоставленные стороны не могут образовать валидный треугольник (не выполняется неравенство треугольника).")

    return side1 + side2 + side3