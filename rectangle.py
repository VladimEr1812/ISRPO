"""
Модуль для вычисления площади и периметра прямоугольника.
"""

def area(length: float, width: float) -> float:
    """
    Вычисляет площадь прямоугольника.

    Принимает:
        length (float): Длина прямоугольника.
        width (float): Ширина прямоугольника.

    Возвращает:
        float: Площадь прямоугольника.

    Вызывает:
        ValueError: Если длина или ширина отрицательны.

    Пример вызова:
    >>> area(10, 5)
    50.0
    """
    if length < 0 or width < 0:
        raise ValueError("Длина и ширина прямоугольника не могут быть отрицательными.")
    return length * width

def perimeter(length: float, width: float) -> float:
    """
    Вычисляет периметр прямоугольника.

    Принимает:
        length (float): Длина прямоугольника.
        width (float): Ширина прямоугольника.

    Возвращает:
        float: Периметр прямоугольника.

    Вызывает:
        ValueError: Если длина или ширина отрицательны.

    Пример вызова:
    >>> perimeter(10, 5)
    30.0
    """
    if length < 0 or width < 0:
        raise ValueError("Длина и ширина прямоугольника не могут быть отрицательными.")
    return 2 * (length + width)